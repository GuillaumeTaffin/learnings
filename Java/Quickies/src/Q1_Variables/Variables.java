package Q1_Variables;

public class Variables {

    public static void main(String[] args) {
        System.out.println("Hello, Java");
        System.out.println("Hello, Java");

        System.out.println("=== Assignation then creation ===");
        String message;
        message = "Hello, World";
        System.out.println(message);
        System.out.println(message);

        System.out.println("=== Assignation and creation ===");
        String name = "Jean Bon";
        System.out.println(name);

        System.out.println("=== Re-Assignation ===");
        String age = "34";
        System.out.println(age);
        age = "25";
        System.out.println(age);
    }

}
